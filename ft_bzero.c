#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	cntr;
	char	*str;

	cntr = 0;
	str = (char *)s;
	while (cntr < n)
	{
		str[cntr] = '\0';
		cntr++;
	}
}
